    table.border = "1";
    table.width = 75 * nCol;					
    table.appendChild(tBody);
    
    for (var i = 0; i < nRows; i++) {
        var tr = document.createElement('TR');
        tBody.appendChild(tr);

        for (var j = 0; j < nCol; j++) {
            var td = document.createElement('TD');
            td.width = "100";
            td.height = "20";
            tr.appendChild(td);
        }
    }
    var c = 0;						//Add employees to table from array. Count every 4th entry to highlight red.
    var d = 0;						//Count number of rows to determine next column.
    var e = 0;
